#include "main.h"
#include <ctype.h>
/**                                                                                                                                         
 * _islower - checks if a char is in lower case or not                                                                                      
 * Return: 1 on true, 0 on false                                                                                                            
 */

int _islower(int c)
{
	return (int) islower(c);
}
